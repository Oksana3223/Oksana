// Calculating the surface area of a parallelepiped
#include <stdio.h>

int main ()
{
  float l,w,h; // length, width and height of a parallelepiped
  float s;   //  surface area of a parallelepiped
  
  printf("\nCalculating the surface area of a parallelepiped\n");
  printf("enter the initial data:\n");
  printf("Length (сm.) -> ");
  scanf("%f", &l);
  printf("Width (сm.) -> ");
  scanf("%f", &w);
  printf("Height (cm.) ->");
  scanf("%f", &h);
   s = (l*w + l*h + w*h)*2;
  printf("Surface area: %6.2f sq.cm.\n", s);
  printf("\n\nTo finish press <Enter>");
  
}
  
